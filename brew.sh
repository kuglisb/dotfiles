brew install --cask android-commandlinetools
# (Android SDK Platform-Tools) Android SDK component
brew install --cask android-platform-tools
# (.NET SDK) Developer platform
brew install --cask dotnet-sdk
# (Mozilla Firefox) Web browser
brew install --cask firefox
# (Flutter SDK) UI toolkit for building applications for mobile, web and desktop
brew install --cask flutter
# (Google Chrome) Web browser
brew install --cask google-chrome
# (Home Assistant) Companion app for Home Assistant home automation software
brew install --cask home-assistant
# (iTerm2) Terminal emulator as alternative to Apple's Terminal app
brew install --cask iterm2
# (Notion) App to write, plan, collaborate, and get organised
brew install --cask notion
# (Slack) Team communication and collaboration software
brew install --cask slack
# (Transmission Remote GUI) [no description]
brew install --cask transmission-remote-gui
# (Microsoft Visual Studio Code, VS Code) Open-source code editor
brew install --cask visual-studio-code
